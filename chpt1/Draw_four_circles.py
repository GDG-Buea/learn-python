# (Turtle: draw four circles) This program draws four circles at the center of the screen,

import turtle

turtle.penup()
turtle.goto(-40, 0)
turtle.pendown()
turtle.circle(45)

turtle.penup()
turtle.goto(49, 0)
turtle.pendown()
turtle.circle(45)

turtle.penup()
turtle.goto(-40, -89 )
turtle.pendown()
turtle.circle(45)

turtle.penup()
turtle.goto(49, -89)
turtle.pendown()
turtle.circle(45)



turtle.done()