# (Turtle: draw a polygon) This program draws a polygon that connects the points
#  ( 40 , -69.28 ), ( -40 , -69.28 ), ( -80 , -9.8 ), ( -40 , 69 ), ( 40 , 69 ), and ( 80 , 0 )

import turtle

turtle.penup()
turtle.goto(-100,200)
turtle.pendown()
turtle.forward(200)

turtle.right(45)
turtle.forward(200)

turtle.right(90)
turtle.forward(200)

turtle.right(45)
turtle.forward(200)

turtle.right(45)
turtle.forward(200)

turtle.right(90)
turtle.forward(200)



turtle.done()